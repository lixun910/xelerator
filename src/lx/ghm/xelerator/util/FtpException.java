package lx.ghm.xelerator.util;

public class FtpException extends Exception {

	public FtpException() {
		super();
	}

	public FtpException(String message) {
		super(message);
	}

	public FtpException(Throwable cause) {
		super(cause);
	}

	public FtpException(String message, Throwable cause) {
		super(message, cause);
	}

}
